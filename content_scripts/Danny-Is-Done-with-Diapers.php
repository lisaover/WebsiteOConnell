        <h2><a href="http://www.albertwhitman.com/content.cfm/bookdetails/Done-with-Diapers">Done with Diapers</a> and <a href="http://www.albertwhitman.com/content.cfm/bookdetails/Danny-Is-Done-with-Diapers">Danny Is Done with Diapers</a></h2>
		<p>Adam avoids accidents. Brianna is busy in the bathroom. Caleb is comfy and clean! Danny and his friends know twenty-six ways to be done with diapers.  The fun, familiar examples and <a href="http://www.saahub.com/artist_results.asp?artistId=90275">Amanda Gulliver</a>'s bright, cheery art help kids practice their potty knowledge and learn their letters, too!</p>
		<p><a href="http://blog.schoollibraryjournal.com/afuse8production/2009/10/21/librarian-preview-albert-whitman-and-company-spring-2010/">Elizabeth Bird  calls <span class="italic">Danny Is Done with Diapers</span> "upbeat" and "smart."</a></p>
		<p>Publisher's Weekly says, "most kids (and parents) should be impressed by the book's comprehensiveness and how author and illustrator achieve a cheerful frankness."</p>
